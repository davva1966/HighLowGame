package au.com.highlowgame;



public class SpeedSolutionsException extends RuntimeException {

	private static final long serialVersionUID = 1L;

	public SpeedSolutionsException() {
		super();
	}

	public SpeedSolutionsException(String message, Throwable cause) {
		super(message, cause);
	}

	public SpeedSolutionsException(String message) {
		super(message);
	}

	public SpeedSolutionsException(Throwable cause) {
		super(cause);
	}
	
}
